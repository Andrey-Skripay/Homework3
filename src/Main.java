public class Main {
    public static void main(String[] args) {

        int aInt = 10000;
        System.out.println("Значение переменной a с типом int равно " + aInt );
        byte bByte = 11;
        System.out.println("Значение переменной a с типом byte равно " + bByte );
        short cShort = -10000;
        System.out.println("Значение переменной a с типом short равно " + cShort );
        long dLong = 1000000L;
        System.out.println("Значение переменной a с типом long равно " + dLong );
        float eFloat = 1.11f;
        System.out.println("Значение переменной a с типом float равно " + eFloat );
        double iDouble = 11.11;
        System.out.println("Значение переменной a с типом double равно " + iDouble );




    }
}